    name,
    type: "normal",
    height,
    limb,
    startup: 12,
    active: 3,
    recovery: 12,
    onHit: 0,
    onBlock: -4,
    onCounterHit: 4,
    damage: 15,
    chipDamage: 2,
    isLauncher: false,
    launchHeight: 0,
    launchGravity: 0,
    isScrew: false,
    isBound: false,
    isHoming: false,
    isPowerCrush: false,
    hasHighCrush: false,
